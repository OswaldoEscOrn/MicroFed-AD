def main():
    print("=" * 80)
    print("CONVOLUTIONAL AUTOENCODER TRAINING PIPELINE")
    print("=" * 80)

    # Configure paths
    base_path = r"D:\Oswaldo's surf project\My Database"
    model_save_path = os.path.join(base_path, "models\\")

    # Ensure directories exist
    os.makedirs(base_path, exist_ok=True)
    os.makedirs(model_save_path, exist_ok=True)

    # 1. Load CONV-AE data (using DeepAE virtual samples)
    X_conv, conv_config, feature_mapping = load_conv_ae_data()
    if X_conv is None:
        print("❌ Failed to load CONV-AE data, exiting process")
        return

    # 2. Prepare training data
    X_train, X_val = prepare_conv_train_val_split(
        X_conv,
        train_ratio=0.85,
        val_ratio=0.15,
        shuffle=True
    )

    # 3. Train CONV Autoencoder
    sequence_length = X_conv.shape[1]
    n_features = X_conv.shape[2]

    autoencoder, encoder, history, total_params = train_conv_autoencoder(
        X_train, X_val,
        sequence_length=sequence_length,
        n_features=n_features,
        epochs=80
    )

    # 4. Evaluate on validation set
    print("\n🔍 Evaluating CONV model on validation set:")
    threshold, val_anomalies_count, val_anomaly_ratio, val_errors, val_reconstructed, _ = evaluate_conv_anomalies(
        autoencoder, X_val, X_val, percentile=95
    )

    # 5. Evaluate on the complete dataset
    print("\n🔍 Evaluating on the complete CONV dataset:")
    X_all = X_conv

    all_reconstructed = autoencoder.predict(X_all, verbose=0)
    all_errors = np.mean((X_all - all_reconstructed) ** 2, axis=(1, 2))

    # Detect anomalies
    all_anomalies = all_errors > threshold
    all_anomalies_count = np.sum(all_anomalies)
    all_anomaly_ratio = all_anomalies_count / len(X_all) * 100

    print(f"  Total samples: {len(X_all):,}")
    print(f"  Anomalies detected: {all_anomalies_count:,}")
    print(f"  Anomaly proportion: {all_anomaly_ratio:.2f}%")

    # 6. Visualize results
    print("\n📊 Generating visualization for all CONV samples:")
    visualize_conv_results(
        history, X_all, all_reconstructed, all_errors, threshold,
        feature_mapping, autoencoder, total_params, model_name="CONV AutoEncoder (35040 samples)"
    )

    # 7. Save models
    save_conv_models(autoencoder, encoder, model_save_path)

    # 🔍 New: Output model performance summary (consistent with LSTM)
    print("\n" + "=" * 80)
    print("📋 CONV-AE Training Completion Report")
    print("=" * 80)

    print(f"\n📊 CONV Model Performance Summary:")
    print(f"  Total samples: {X_conv.shape[0]:,}")
    print(f"  Actual training samples used: {X_train.shape[0]:,}")
    print(f"  Actual validation samples used: {X_val.shape[0]:,}")
    print(f"  Total params: {total_params:,}")
    print(f"  Validation loss (MSE): {history.history['val_loss'][-1]:.6f}")
    print(f"  95th percentile threshold (validation): {threshold:.6f}")
    print(f"  Anomaly detection (all samples): {all_anomalies_count:,} / {len(X_all):,} ({all_anomaly_ratio:.2f}%)")

    # 8. Generate analysis report
    conv_results = {
        'total_params': total_params,
        'val_mae': history.history.get('val_mae', [0])[-1],
        'val_loss': history.history['val_loss'][-1],
        'all_anomaly_ratio': all_anomaly_ratio,
        'all_anomalies_count': all_anomalies_count,
        'threshold': threshold
    }

