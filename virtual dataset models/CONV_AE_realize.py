# ====================== 2. Build Conv-AE model ======================
def build_conv_autoencoder(sequence_length=24, n_features=11,
                           filters=[32, 64, 128], kernel_size=3,
                           encoding_dim=32):
    """
    Build a 1D Convolutional AutoEncoder (Conv1D AutoEncoder)

    Parameters:
        sequence_length: sequence length (24 hours)
        n_features: number of features
        filters: number of filters per convolutional layer
        kernel_size: kernel size
        encoding_dim: encoding dimension
    """
    print(f"\n🔧 Building CONV-AutoEncoder model:")
    print(f"  Input shape: ({sequence_length}, {n_features})")
    print(f"  Convolutional filters: {filters}")
    print(f"  Encoding dimension: {encoding_dim}")

    # ====== Encoder ======
    inputs = Input(shape=(sequence_length, n_features), name='conv_input')

    x = inputs

    # Convolutional encoding layers
    for i, f in enumerate(filters):
        x = Conv1D(filters=f, kernel_size=kernel_size, activation='relu',
                   padding='same', name=f'encoder_conv{i + 1}')(x)
        x = BatchNormalization(name=f'encoder_bn{i + 1}')(x)
        x = MaxPooling1D(pool_size=2, padding='same', name=f'encoder_pool{i + 1}')(x)
        x = Dropout(0.2, name=f'encoder_dropout{i + 1}')(x)

    # Flatten and encode
    x = Flatten(name='encoder_flatten')(x)
    encoded = Dense(encoding_dim, activation='relu', name='bottleneck')(x)

    # ====== Decoder ======
    # Reshape to the shape required by convolutional layers
    decoded = Dense(x.shape[1], activation='relu', name='decoder_dense')(encoded)

    # Need to compute the shape after reshaping
    # Compute the dimensions after encoder pooling
    conv_shape = None
    for i, f in enumerate(filters):
        if i == 0:
            conv_shape = (sequence_length // (2 ** (i + 1)), f)
        else:
            conv_shape = (conv_shape[0] // 2, f)

    decoded = Reshape(conv_shape, name='decoder_reshape')(decoded)

    # Deconvolutional layers (reverse order)
    for i, f in enumerate(reversed(filters)):
        decoded = Conv1D(filters=f, kernel_size=kernel_size, activation='relu',
                         padding='same', name=f'decoder_conv{i + 1}')(decoded)
        decoded = BatchNormalization(name=f'decoder_bn{i + 1}')(decoded)
        decoded = UpSampling1D(size=2, name=f'decoder_upsample{i + 1}')(decoded)

    # Final convolutional layer, output same shape as input
    outputs = Conv1D(filters=n_features, kernel_size=kernel_size,
                     activation='linear', padding='same', name='output')(decoded)

    # ====== Complete model ======
    autoencoder = Model(inputs, outputs, name='conv_autoencoder')

    # Encoder model (for feature extraction)
    encoder = Model(inputs, encoded, name='conv_encoder')

    # Calculate parameters
    total_params = autoencoder.count_params()
    trainable_params = np.sum([np.prod(v.shape) for v in autoencoder.trainable_weights])
    non_trainable_params = total_params - trainable_params

    print(f"\n📊 Initial model parameters count:")
    print(f"  Total parameters: {total_params:,}")
    print(f"  Trainable parameters: {trainable_params:,}")
    print(f"  Non-trainable parameters: {non_trainable_params:,}")
    # print(f"  Target parameters: ~8000 (Similar to the Conv-AE in the paper)")

    return autoencoder, encoder, total_params


# ====================== 3. Build improved Conv-AE (with residual connections) ======================
def build_residual_conv_ae(sequence_length=24, n_features=11):
    """
    Build a Conv-AE with residual connections, closer to the paper's structure
    """
    print(f"\n🔧 Building improved Residual CONV-AE:")
    print(f"  Input shape: ({sequence_length}, {n_features})")

    inputs = Input(shape=(sequence_length, n_features))

    # ====== Encoder ======
    # First convolutional block
    x = Conv1D(32, kernel_size=5, padding='same', activation='relu')(inputs)
    x = BatchNormalization()(x)
    x = MaxPooling1D(pool_size=2, padding='same')(x)

    # Second convolutional block
    x = Conv1D(64, kernel_size=3, padding='same', activation='relu')(x)
    x = BatchNormalization()(x)
    x = MaxPooling1D(pool_size=2, padding='same')(x)

    # Third convolutional block
    x = Conv1D(128, kernel_size=3, padding='same', activation='relu')(x)
    x = BatchNormalization()(x)
    encoded = MaxPooling1D(pool_size=2, padding='same')(x)

    # ====== Decoder ======
    # First deconvolutional block
    x = Conv1D(128, kernel_size=3, padding='same', activation='relu')(encoded)
    x = BatchNormalization()(x)
    x = UpSampling1D(size=2)(x)

    # Second deconvolutional block
    x = Conv1D(64, kernel_size=3, padding='same', activation='relu')(x)
    x = BatchNormalization()(x)
    x = UpSampling1D(size=2)(x)

    # Third deconvolutional block
    x = Conv1D(32, kernel_size=3, padding='same', activation='relu')(x)
    x = BatchNormalization()(x)
    x = UpSampling1D(size=2)(x)

    # Output layer
    outputs = Conv1D(n_features, kernel_size=5, padding='same', activation='linear')(x)

    # ====== Complete model ======
    autoencoder = Model(inputs, outputs, name='residual_conv_ae')
    encoder = Model(inputs, encoded, name='residual_conv_encoder')

    # Calculate parameters
    total_params = autoencoder.count_params()
    trainable_params = np.sum([np.prod(v.shape) for v in autoencoder.trainable_weights])
    non_trainable_params = total_params - trainable_params

    print(f"\n📊 Optimized model parameters count:")
    print(f"  Total parameters: {total_params:,}")
    print(f"  Trainable parameters: {trainable_params:,}")
    print(f"  Non-trainable parameters: {non_trainable_params:,}")
    print(f"  Target parameters: ~8000 (Similar to the Conv-AE in the paper)")

    return autoencoder, encoder, total_params


# ====================== 4. Data preparation function ======================
def prepare_conv_train_val_split(X_conv, train_ratio=0.85, val_ratio=0.15, shuffle=True):
    """
    Prepare training and validation data for CONV-AE
    """
    n_samples = X_conv.shape[0]

    if shuffle:
        # Random shuffle
        indices = np.random.permutation(n_samples)
        X_shuffled = X_conv[indices]
    else:
        X_shuffled = X_conv

    # Split training and validation sets
    train_end = int(n_samples * train_ratio)
    val_end = train_end + int(n_samples * val_ratio)

    X_train = X_shuffled[:train_end]
    X_val = X_shuffled[train_end:val_end]

    print(f"\n📊 CONV-AE dataset splitting:")
    print(f"  Training Set: {X_train.shape[0]:,} samples ({train_ratio * 100:.1f}%)")
    print(f"  Validation Set: {X_val.shape[0]:,} samples ({val_ratio * 100:.1f}%)")
    print(f"  Total Samples: {n_samples:,}")

    return X_train, X_val


# ====================== 5. Model training ======================
def train_conv_autoencoder(X_train, X_val, sequence_length, n_features, epochs=80):
    """
    Train CONV Autoencoder
    """
    print(f"\n🚀 Starting training of CONV-AE...")

    # Build model - using improved version, parameters closer to paper
    autoencoder, encoder, total_params = build_residual_conv_ae(
        sequence_length=sequence_length,
        n_features=n_features
    )

    # Compile model
    autoencoder.compile(
        optimizer=Adam(learning_rate=0.001),
        loss='mse',
        metrics=['mae']
    )

    print("\n📋 Model Architecture Summary:")
    autoencoder.summary()

    # Callbacks
    callbacks = [
        # EarlyStopping(
        #     monitor='val_loss',
        #     patience=15,
        #     restore_best_weights=True,
        #     verbose=1
        # ),
        ReduceLROnPlateau(
            monitor='val_loss',
            factor=0.5,
            patience=10,
            min_lr=1e-6,
            verbose=1
        )
    ]

    # Train model
    print(f"\n📈 Training Details:")
    print(f"  Batch Size: 128")
    print(f"  Max Epochs: {epochs}")

    history = autoencoder.fit(
        X_train, X_train,
        validation_data=(X_val, X_val),
        epochs=epochs,
        batch_size=128,
        # callbacks=callbacks,
        verbose=1
    )

    return autoencoder, encoder, history, total_params


# ====================== 6. Evaluation and anomaly detection ======================
def evaluate_conv_anomalies(autoencoder, X_val, X_test=None, percentile=95):
    """
    Evaluate CONV-AE model and detect anomalies
    """
    print("\n📈 CONV-AE Model Evaluation Results:")

    # Evaluate on validation set
    val_loss, val_mae = autoencoder.evaluate(X_val, X_val, verbose=0)
    print(f"  Validation loss (MSE): {val_loss:.6f}")
    print(f"  Validation MAE: {val_mae:.6f}")

    # Calculate validation set reconstruction errors
    val_reconstructed = autoencoder.predict(X_val, verbose=0)
    val_errors = np.mean((X_val - val_reconstructed) ** 2, axis=(1, 2))

    # Calculate threshold (95th percentile)
    threshold = np.percentile(val_errors, percentile)
    print(f"  Validation set reconstruction error {percentile}th percentile threshold: {threshold:.6f}")

    # Detect anomalies on validation set (crucial: add this output)
    val_anomalies = val_errors > threshold
    val_anomalies_count = np.sum(val_anomalies)
    val_anomaly_ratio = val_anomalies_count / len(X_val) * 100

    print(f"  Detected {val_anomalies_count} anomalous samples out of {len(X_val)} total samples")
    print(f"  Anomaly ratio: {val_anomaly_ratio:.2f}%")

    # If test set is provided
    if X_test is not None:
        test_reconstructed = autoencoder.predict(X_test, verbose=0)
        test_errors = np.mean((X_test - test_reconstructed) ** 2, axis=(1, 2))

        anomalies = test_errors > threshold
        anomalies_count = np.sum(anomalies)
        anomaly_ratio = anomalies_count / len(X_test) * 100

        return threshold, val_anomalies_count, val_anomaly_ratio, test_errors, test_reconstructed, val_errors
    else:
        # Note: return validation set anomaly count and ratio here
        return threshold, val_anomalies_count, val_anomaly_ratio, val_errors, val_reconstructed, val_errors


